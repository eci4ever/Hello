// Expensive Calculator
// Demonstrate Built-in arithmetic operators

#include <iostream>

using namespace std;

int main () {

	cout << "10 + 3 = " << 10+3 << endl;
	cout << "10 - 3 = " << 10-3 << endl;
	cout << "10 * 3 = " << 10*3 << endl;
	cout << "7 / 3 = " << 7/3 << endl;
	cout << "7.0 / 3.0 = " << 7.0/3.0 << endl;
	cout << "7 % 3 = " << 7%3 << endl;

	return 0;
}
